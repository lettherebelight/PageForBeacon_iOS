- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender
{
    if ([segue.identifier isEqualToString:@"showCouponDetail"]) {
        NSIndexPath *indexPath = [self.tableView indexPathForSelectedRow];
        HAMCouponDetailViewController *destVC = segue.destinationViewController;
        destVC.hidesBottomBarWhenPushed = YES;
        if ([destVC respondsToSelector:@selector(setHomepage:)]) {
            HAMHomepage *page = [homepageVisitedRecords objectAtIndex:indexPath.row];
            if (page) {
                [destVC setValue:page forKey:@"homepage"];
            }
        }
    }
}

